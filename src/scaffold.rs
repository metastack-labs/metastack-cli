use std::path::{Path, PathBuf};

use anyhow::Result;

use crate::backlog::template_seed_files;
use crate::cli::ScaffoldArgs;
use crate::fs::{
    FileWriteStatus, PlanningPaths, canonicalize_existing_dir, display_path, ensure_dir,
    write_text_file,
};

#[derive(Debug, Clone)]
pub struct ScaffoldReport {
    root: PathBuf,
    created_dirs: Vec<String>,
    created_files: Vec<String>,
    updated_files: Vec<String>,
    reused_paths: Vec<String>,
}

impl ScaffoldReport {
    pub fn render(&self) -> String {
        let mut lines = vec![format!(
            "Scaffolded MetaStack workspace at {}",
            self.root.display()
        )];
        append_section(&mut lines, "Created directories", &self.created_dirs);
        append_section(&mut lines, "Created files", &self.created_files);
        append_section(&mut lines, "Updated files", &self.updated_files);
        append_section(&mut lines, "Reused paths", &self.reused_paths);
        lines.join("\n")
    }
}

pub fn run_scaffold(args: &ScaffoldArgs) -> Result<ScaffoldReport> {
    let root = canonicalize_existing_dir(&args.root)?;
    ensure_layout(&root, args.force, true)
}

pub fn ensure_planning_layout(root: &Path, force: bool) -> Result<()> {
    let _ = ensure_layout(root, force, true)?;
    Ok(())
}

fn ensure_layout(root: &Path, force: bool, include_cron: bool) -> Result<ScaffoldReport> {
    let paths = PlanningPaths::new(root);
    migrate_legacy_agent_layout(&paths)?;
    let mut report = ScaffoldReport {
        root: root.to_path_buf(),
        created_dirs: Vec::new(),
        created_files: Vec::new(),
        updated_files: Vec::new(),
        reused_paths: Vec::new(),
    };

    let mut dirs = vec![
        &paths.metastack_dir,
        &paths.agent_dir,
        &paths.agent_briefs_dir,
        &paths.agent_sessions_dir,
        &paths.backlog_dir,
        &paths.codebase_dir,
        &paths.workflows_dir,
    ];
    if include_cron {
        dirs.push(&paths.cron_dir);
    }

    for dir in dirs {
        if ensure_dir(dir)? {
            report.created_dirs.push(display_path(dir, root));
        } else {
            report.reused_paths.push(display_path(dir, root));
        }
    }

    for (path, contents) in templates(&paths, include_cron)
        .into_iter()
        .chain(template_seed_files(&paths).into_iter())
    {
        match write_text_file(&path, &contents, force)? {
            FileWriteStatus::Created => report.created_files.push(display_path(&path, root)),
            FileWriteStatus::Updated => report.updated_files.push(display_path(&path, root)),
            FileWriteStatus::Unchanged => report.reused_paths.push(display_path(&path, root)),
        }
    }

    Ok(report)
}

pub fn ensure_backlog_templates(root: &Path, force: bool) -> Result<()> {
    ensure_planning_layout(root, false)?;

    let paths = PlanningPaths::new(root);
    ensure_dir(&paths.backlog_template_dir)?;

    for (path, contents) in template_seed_files(&paths) {
        let _ = write_text_file(&path, &contents, force)?;
    }

    Ok(())
}

fn append_section(lines: &mut Vec<String>, title: &str, entries: &[String]) {
    if entries.is_empty() {
        return;
    }

    lines.push(format!("{title}:"));
    for entry in entries {
        lines.push(format!("  - {entry}"));
    }
}

