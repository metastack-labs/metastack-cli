fn migrate_legacy_agent_layout(paths: &PlanningPaths) -> Result<()> {
    for (legacy, canonical) in [
        (paths.legacy_agent_dir(), paths.agent_dir.clone()),
        (
            paths.legacy_agent_briefs_dir(),
            paths.agent_briefs_dir.clone(),
        ),
        (
            paths.legacy_agent_sessions_dir(),
            paths.agent_sessions_dir.clone(),
        ),
    ] {
        if !legacy.exists() || canonical.exists() {
            continue;
        }

        if std::fs::rename(&legacy, &canonical).is_ok() {
            continue;
        }

        if legacy.is_file() {
            std::fs::copy(&legacy, &canonical).map(|_| ())?;
        } else {
            copy_dir_contents(&legacy, &canonical)?;
        }
    }

    Ok(())
}

fn copy_dir_contents(source: &Path, destination: &Path) -> Result<()> {
    ensure_dir(destination)?;
    for entry in std::fs::read_dir(source)? {
        let entry = entry?;
        let source_path = entry.path();
        let destination_path = destination.join(entry.file_name());
        if entry.file_type()?.is_dir() {
            copy_dir_contents(&source_path, &destination_path)?;
        } else {
            if let Some(parent) = destination_path.parent() {
                ensure_dir(parent)?;
            }
            std::fs::copy(&source_path, &destination_path)?;
        }
    }

    Ok(())
}
