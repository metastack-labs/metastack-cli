use std::fs;
use std::path::{Path, PathBuf};

use walkdir::WalkDir;

fn main() {
    let manifest_dir =
        PathBuf::from(std::env::var("CARGO_MANIFEST_DIR").expect("CARGO_MANIFEST_DIR not set"));
    let out_dir = PathBuf::from(std::env::var("OUT_DIR").expect("OUT_DIR not set"));

    println!("cargo:rerun-if-changed=Cargo.toml");

    // --- Parse branding from Cargo.toml ---
    let cargo_toml_path = manifest_dir.join("Cargo.toml");
    let cargo_toml_contents =
        fs::read_to_string(&cargo_toml_path).expect("failed to read Cargo.toml");
    let cargo_toml: toml::Table =
        toml::from_str(&cargo_toml_contents).expect("failed to parse Cargo.toml");

    let branding = cargo_toml
        .get("package")
        .and_then(|p| p.get("metadata"))
        .and_then(|m| m.get("branding"))
        .expect("[package.metadata.branding] section missing from Cargo.toml");

    let command_name = branding
        .get("command_name")
        .and_then(toml::Value::as_str)
        .expect("branding.command_name missing");
    let project_dir = branding
        .get("project_dir")
        .and_then(toml::Value::as_str)
        .expect("branding.project_dir missing");
    let display_name = branding
        .get("display_name")
        .and_then(toml::Value::as_str)
        .expect("branding.display_name missing");

    println!("cargo:rustc-env=BRAND_COMMAND_NAME={command_name}");
    println!("cargo:rustc-env=BRAND_PROJECT_DIR={project_dir}");
    println!("cargo:rustc-env=BRAND_DISPLAY_NAME={display_name}");

    // --- Expand placeholders in src/artifacts/**/*.md and write to OUT_DIR ---
    let artifacts_dir = manifest_dir.join("src").join("artifacts");
    let out_artifacts_dir = out_dir.join("artifacts");

    for entry in WalkDir::new(&artifacts_dir)
        .into_iter()
        .filter_map(Result::ok)
        .filter(|e| e.file_type().is_file())
        .filter(|e| e.path().extension().is_some_and(|ext| ext == "md"))
    {
        let source_path = entry.path();
        let relative = source_path
            .strip_prefix(&artifacts_dir)
            .expect("artifact path should be under artifacts_dir");

        println!("cargo:rerun-if-changed={}", source_path.display());

        let contents = fs::read_to_string(source_path)
            .unwrap_or_else(|e| panic!("failed to read {}: {e}", source_path.display()));

        let expanded = contents
            .replace("{{brand.command_name}}", command_name)
            .replace("{{brand.project_dir}}", project_dir)
            .replace("{{brand.display_name}}", display_name);

        let dest_path = out_artifacts_dir.join(relative);
        if let Some(parent) = dest_path.parent() {
            fs::create_dir_all(parent)
                .unwrap_or_else(|e| panic!("failed to create {}: {e}", parent.display()));
        }
        fs::write(&dest_path, expanded)
            .unwrap_or_else(|e| panic!("failed to write {}: {e}", dest_path.display()));
    }

    // Also emit rerun-if-changed for the artifacts directory itself so new files trigger rebuilds
    emit_rerun_for_dirs(&artifacts_dir);

    // --- Generate CLI help text with branding interpolation ---
    generate_cli_help(&out_dir, command_name, project_dir);
}

