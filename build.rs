fn emit_rerun_for_dirs(dir: &Path) {
    if !dir.is_dir() {
        return;
    }
    println!("cargo:rerun-if-changed={}", dir.display());
    for entry in fs::read_dir(dir).into_iter().flatten().flatten() {
        if entry.file_type().is_ok_and(|t| t.is_dir()) {
            emit_rerun_for_dirs(&entry.path());
        }
    }
}
